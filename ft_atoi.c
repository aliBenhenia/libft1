#include "libft.h"

int	ft_atoi(const char *s)
{
	long long	r;
	int	sign;

	r = 0;
	sign = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
	{
		s++;
	}
	while (*s == '+' || *s == '-')
	{
		if (*s == '-')
		{
			sign = sign * -1;
		}
		s++;
	}
	while ((*s >= '0' && *s <= '9'))
	{

		if (r * sign > 2147483647)
			return (-1);
		else if (r * sign < -2147483648)
			return (0);
		r = r * 10 + *s - '0';
		s++;
	}
	return (r * sign);
}
